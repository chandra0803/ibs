package com.ibsplc.amadeus.vo;

public class FreetextDetailVO  extends AbstractVO {
	
	private String subjectQualifier;
	private String type;
	private String companyId;
	private String language;
	
	
	
	/**
	 * @return the subjectQualifier
	 */
	public String getSubjectQualifier() {
		return subjectQualifier;
	}
	/**
	 * @param subjectQualifier the subjectQualifier to set
	 */
	public void setSubjectQualifier(String subjectQualifier) {
		this.subjectQualifier = subjectQualifier;
	}
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	/**
	 * @return the companyId
	 */
	public String getCompanyId() {
		return companyId;
	}
	/**
	 * @param companyId the companyId to set
	 */
	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}
	/**
	 * @return the language
	 */
	public String getLanguage() {
		return language;
	}
	/**
	 * @param language the language to set
	 */
	public void setLanguage(String language) {
		this.language = language;
	}
	
	

}
