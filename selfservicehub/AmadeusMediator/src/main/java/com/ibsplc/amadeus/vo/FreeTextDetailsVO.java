package com.ibsplc.amadeus.vo;

public class FreeTextDetailsVO extends AbstractVO {
	
	private String textSubjectQualifier;
	private String informationType;
	private String companyId;
	private String language;
	private String source;
	private String encoding;
	private String subjectQualifier;
	private String type;
  
	
	/**
	 * @return the textSubjectQualifier
	 */
	public String getTextSubjectQualifier() {
		return textSubjectQualifier;
	}
	/**
	 * @param textSubjectQualifier the textSubjectQualifier to set
	 */
	public void setTextSubjectQualifier(String textSubjectQualifier) {
		this.textSubjectQualifier = textSubjectQualifier;
	}
	/**
	 * @return the informationType
	 */
	public String getInformationType() {
		return informationType;
	}
	/**
	 * @param informationType the informationType to set
	 */
	public void setInformationType(String informationType) {
		this.informationType = informationType;
	}
	/**
	 * @return the companyId
	 */
	public String getCompanyId() {
		return companyId;
	}
	/**
	 * @param companyId the companyId to set
	 */
	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}
	/**
	 * @return the language
	 */
	public String getLanguage() {
		return language;
	}
	/**
	 * @param language the language to set
	 */
	public void setLanguage(String language) {
		this.language = language;
	}
	/**
	 * @return the source
	 */
	public String getSource() {
		return source;
	}
	/**
	 * @param source the source to set
	 */
	public void setSource(String source) {
		this.source = source;
	}
	/**
	 * @return the encoding
	 */
	public String getEncoding() {
		return encoding;
	}
	/**
	 * @param encoding the encoding to set
	 */
	public void setEncoding(String encoding) {
		this.encoding = encoding;
	}
	/**
	 * @return the subjectQualifier
	 */
	public String getSubjectQualifier() {
		return subjectQualifier;
	}
	/**
	 * @param subjectQualifier the subjectQualifier to set
	 */
	public void setSubjectQualifier(String subjectQualifier) {
		this.subjectQualifier = subjectQualifier;
	}
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	} 
	 
	
	
}
